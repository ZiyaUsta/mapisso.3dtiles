package com.mapisso.model.tiles3D;

public class Asset {
	
	private String version;
	
	private String tilesetVersion;
	
	private String gltfUpAxis;

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getTilesetVersion() {
		return tilesetVersion;
	}

	public void setTilesetVersion(String tilesetVersion) {
		this.tilesetVersion = tilesetVersion;
	}

	public String getGltfUpAxis() {
		return gltfUpAxis;
	}

	public void setGltfUpAxis(String gltfUpAxis) {
		this.gltfUpAxis = gltfUpAxis;
	}
	
	

}
